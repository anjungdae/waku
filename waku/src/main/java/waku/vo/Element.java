package waku.vo;

public class Element {
	int cNo;
	int iNo;
	int iReq;
	
	public int getcNo() {
		return cNo;
	}
	public void setcNo(int cNo) {
		this.cNo = cNo;
	}
	public int getiNo() {
		return iNo;
	}
	public void setiNo(int iNo) {
		this.iNo = iNo;
	}
	public int getiReq() {
		return iReq;
	}
	public void setiReq(int iReq) {
		this.iReq = iReq;
	}
}
