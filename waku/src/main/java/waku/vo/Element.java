package waku.vo;

public class Element {
	int gNo;
	int iNo;
	int iReq;
	
	public int getgNo() {
		return gNo;
	}
	public void setgNo(int gNo) {
		this.gNo = gNo;
	}
	public int getiNo() {
		return iNo;
	}
	public void setiNo(int iNo) {
		this.iNo = iNo;
	}
	public int getiReq() {
		return iReq;
	}
	public void setiReq(int iReq) {
		this.iReq = iReq;
	}
}
